        /// Any kind of natural solid ground - like sand piles, clay piles, farm plots, etc.
        /// </summary>
        SolidGround,

        /// <summary>
        /// Bushes, grasses, mushrooms, etc.
        /// </summary>
        Vegetation,

        /// <summary>
        /// Any kind of objects made out of wood or for trees.
        /// </summary>
        Wood,

        /// <summary>
        /// Any kind of objects made out of stone or for stones in the world.
        /// </summary>
        Stone,

        /// <summary>
        /// Any kind of metallic objects such as metal walls, robots, metal containers, etc.
        /// </summary>
        Metal,

        /// <summary>
        /// Any kind of glass object such as windows, showcases, etc.
        /// </summary>
        Glass
    }
}